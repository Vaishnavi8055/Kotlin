package Operators

fun main() {
    var number1 = 14.8
    val number2 = 2.2

    number1 += number2
    println(number1)

    var number3 = 78.2

    number3 -= number2
    println(number3)

    var number4 = 12.4

    number4 *= number3
    println(number4)

    var number5 = 21.4

    number5 /= number4
    println(number5)

    var number6 = 2.9

    number6 %= number5
    println(number6)
}