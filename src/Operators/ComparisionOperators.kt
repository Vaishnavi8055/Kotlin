package Operators

fun main() {
    val number1 = 14.8
    val number2 = 6.4

    var result: Boolean

    result = number1 == number2
    println(result)

    result = number1 != number2
    println(result)

    result = number1 <= number2
    println(result)

    result = number1 >= number2
    println(result)

    result = number1 > number2
    println(result)

    result = number1 < number2
    println(result)

}