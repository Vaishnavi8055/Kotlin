fun main() {
    var number1 = 10
    var number2 = 5

    number1++            /* postfix Increment and Decrement Operator */
    println(number1)

    number1--
    println(number1)

    number2--
    println(number2)

    number2++
    println(number2)

    println()

    ++number1           /* prefix Increment and Decrement Operator */
    println(number1)

    --number1
    println(number1)

    --number2
    println(number2)

    ++number2
    println(number2)

}